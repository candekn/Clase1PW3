using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BibliotecaDeClases
{
    public class Calculadora
    {
        /// <summary>
        /// Suma dos numeros enteros y devuelve el resultado 
        /// </summary>
        /// <param name="n1">Ingrese un numero entero</param>
        /// <param name="n2">Ingrese otro numero entero</param>
        /// <returns>El resultado de la suma/returns>
        public int Sumar(int n1, int n2)
        {
            return n1 + n2;
        }
    }
}
